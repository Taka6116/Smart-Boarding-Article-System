const SLUG_PREFIX = 'smartboarding-'

function sanitizeSlugSegment(raw: string): string {
  return normalizeMaInSlug(
    raw
      .trim()
      .toLowerCase()
      .replace(/[^a-z0-9\s-]/g, '')
      .replace(/\s+/g, '-')
      .replace(/-+/g, '-')
      .replace(/^-|-$/g, '')
      .slice(0, 60)
  )
}

/** 記事タイトル・本文・キーワードから SEO 向け英語スラッグを生成する（先頭は常に smartboarding- 固定、続きは本文内容に即した固有部分） */
export async function generateSlugFromGemini(
  title: string,
  targetKeyword?: string,
  articleBody?: string
): Promise<string> {
  const apiKey = process.env.GEMINI_API_KEY
  if (!apiKey?.trim()) return fallbackSlug()

  const kwPart = targetKeyword?.trim() ? `\nTarget keyword: ${targetKeyword.trim()}` : ''
  const bodyExcerpt = (articleBody ?? '').trim().slice(0, 2000)
  const bodyPart = bodyExcerpt
    ? `\nArticle body (Japanese excerpt — use this to capture the specific topic and wording):\n${bodyExcerpt}`
    : ''

  const prompt = `You are an SEO specialist. The URL slug MUST start with the fixed prefix "smartboarding-" followed by a UNIQUE English segment for this article.

You will output ONLY the variable segment: the part that comes AFTER "smartboarding-". Do NOT include "smartboarding" or "smartboarding-" in your output.

CRITICAL: Respond in English only. NEVER use Japanese characters. Output ONLY that segment, nothing else.

RULES for the variable segment (after the fixed prefix):
- Use ONLY lowercase English letters (a-z), numbers (0-9), and hyphens (-)
- 3 to 5 words separated by hyphens
- Reflect the SPECIFIC topic using BOTH the title AND the article body excerpt below (body is primary for uniqueness)
- Do NOT use Japanese, Chinese, or any non-ASCII characters
- NEVER use the standalone token "sme" — spell it out as "small-business"
- The fixed prefix "smartboarding-" already states the product; avoid repeating "smartboarding" or "boarding" inside the variable segment
- Prefer concrete topic terms: e-learning, lms, training-design, onboarding, leadership-dev, talent-management, skills, evaluation, etc.

EXAMPLES (output = variable segment only):
- Title/body about LMS introduction → lms-introduction-benefits
- Title/body about new employee onboarding training → onboarding-training-design
- Title/body about manager training → leadership-training-remote
- Title/body about measuring training effectiveness → training-roi-metrics-guide
- Title/body about HR evaluation and learning linkage → talent-evaluation-learning-cycle

Title: ${title.trim()}${kwPart}${bodyPart}

Variable segment after "smartboarding-":`

  try {
    const raw = await generateContentWithFallback(apiKey, prompt)
    let segment = sanitizeSlugSegment(raw)
    if (segment.startsWith('smartboarding-')) {
      segment = segment.slice('smartboarding-'.length).replace(/^-+/, '')
    }
    segment = segment.replace(/^smartboarding-?/, '').replace(/^-+/, '')
    if (segment.startsWith('ma-advisor-')) {
      segment = segment.slice('ma-advisor-'.length).replace(/^-+/, '')
    }
    segment = segment.replace(/^ma-advisor-?/, '').replace(/^-+/, '')
    segment = polishAutoGeneratedSlugVariableSegment(segment)
    const full = segment.length >= 3 ? `${SLUG_PREFIX}${segment}` : fallbackSlug()
    return normalizeMaInSlug(full)
  } catch (e) {
    console.warn('Gemini slug generation failed:', (e as Error)?.message)
    return fallbackSlug()
  }
}

